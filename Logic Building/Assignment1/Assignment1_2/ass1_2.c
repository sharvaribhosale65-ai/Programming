/////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////
//
// Function name    :   Display
// Description  :       Print "Marvellous" 5 times
// Date :               26/05/2026 
// Author   :           Sharvari Gorakhnath Bhosale
//
/////////////////////////////////////////////////////////////////////////////

void Display()
{
    int i = 0;                   

    for(i=1; i<=5; i++)
    {
        printf("Marvellous\n");
    }
}

//////////////////////////////////////////////////////////////////////////////
//
// Application to print "Marvellous" 
//
/////////////////////////////////////////////////////////////////////////////

int main()
{
    Display();

    return 0;
}

