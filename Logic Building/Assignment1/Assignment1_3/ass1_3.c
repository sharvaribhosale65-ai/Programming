/////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////
//
// Function name    :   Display
// Description  :       Print 5 to 1 numbers
// Date :               26/05/2026 
// Author   :           Sharvari Gorakhnath Bhosale
//
/////////////////////////////////////////////////////////////////////////////

void Display()
{
    int i = 0;

    i = 5;
    while(i>=1)
    {
        printf("%d",i);
        i--;
    }
}

/////////////////////////////////////////////////////////////////////////////
//
// Application to print 5 to 1 numbers
//
/////////////////////////////////////////////////////////////////////////////


int main()
{

    Display();
     
    return 0;
}

