/*
Algorithm:

    START
        Accept first number as iStart
        Accept second number as iEnd
        Perform to display all numbers of that range in reverse order
        Display the result
    STOP
*/

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Include required header files
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name    :   RangeDisplayReverse
// Input    :           int, int
// Output   :           int
// Description  :       Perform to display all numbers of that range in reverse order
// Date :               29/05/2026 
// Author   :           Sharvari Gorakhnath Bhosale
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


void RangeDisplayReverse(
                            int iStart,                             //first input
                            int iEnd                                //second input
                        )
{
    int iCnt = 0;

    if(iStart > iEnd)                                               
    {
        printf("Invalid range");
    }

    for(iCnt = iEnd ; iCnt >= iStart; iCnt--)
    {
        printf("%d\t",iCnt);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application to perform all numbers of that range  in reverse order of 2 int values
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


int main()
{
    int iValue1 = 0;                                                    //to store first input
    int iValue2 = 0;                                                    //to store second input

    printf("Enter starting point : ");
    scanf("%d",&iValue1);

    printf("Enter ending point : ");
    scanf("%d",&iValue2);

    RangeDisplayReverse(iValue1, iValue2);                              //display result

    return 0;
}

