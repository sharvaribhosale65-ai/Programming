/*
Algorithm:

    START
        Accept first number as iStart
        Accept second number as iEnd
        Perform to display all numbers of that range
        Display the result
    STOP
*/

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Include required header files
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name    :   RangeDisplay
// Input    :           int, int
// Output   :           int
// Description  :       Perform to display all numbers of that range
// Date :               29/05/2026 
// Author   :           Sharvari Gorakhnath Bhosale
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void RangeDisplay   (
                        int iStart,                     //first input
                        int iEnd                        //second input
                    )
{
    int iCnt = 0;                                       //initialize count

    if(iStart > iEnd)                                   //set filter
    {
        printf("Invalid range");
    }
    
    for(iCnt = iStart; iCnt <= iEnd; iCnt++)            //perform loop
    {
        printf("%d\t",iCnt);
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application to perform all numbers of that range of 2 int values
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0;                                    //to store first input
    int iValue2 = 0;                                    //to store second input

    printf("Enter starting point : ");                  //accept first input from user
    scanf("%d",&iValue1);

    printf("Enter ending point : ");                    //accept second input from user
    scanf("%d",&iValue2);

    RangeDisplay(iValue1, iValue2);                     //display result

    return 0;
}

