/*
Algorithm:

    START
        Accept first number as iStart
        Accept second number as iEnd
        Perform to display all even numbers of that range
        Display the result
    STOP
*/

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Include required header files
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name    :   RangeDisplayEven
// Input    :           int, int
// Output   :           int
// Description  :       Perform to display all even numbers of that range
// Date :               29/05/2026 
// Author   :           Sharvari Gorakhnath Bhosale
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


void RangeDisplayEven(int iStart, int iEnd)
{
    int iCnt = 0;

    if(iStart > iEnd )
    {
        printf("Invalid range");
    }

    for(iCnt = iStart; iCnt <= iEnd; iCnt++)
    {
        if(iCnt % 2 == 0)
        {
            printf("%d\t",iCnt);
        }
    }
}

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application to perform all even numbers of that range of 2 int values
//
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue1 = 0;
    int iValue2 = 0;

    printf("Enter starting point : ");
    scanf("%d",&iValue1);

    printf("Enter ending point : ");
    scanf("%d",&iValue2);

    RangeDisplayEven(iValue1, iValue2);

    return 0;
}

