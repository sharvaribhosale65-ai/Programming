/*
Algorithm:

    START
        Accept number as iNo
        Perform that number of $ and * on screen
        Display the result
    STOP
*/

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Include required header files
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name    :   Pattern
// Input    :           int
// Output   :           int
// Description  :       Perform that number of $ and * on screen
// Date :               30/05/2026 
// Author   :           Sharvari Gorakhnath Bhosale
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Pattern(
                int iNo                         //input
            )
{
    int iCnt = 0;                               //initialize counter

    if(iNo < 0)                                 //updator
    {
        iNo = -iNo;
    }

    for(iCnt = 1; iCnt <= iNo; iCnt++)          //perform loop
    {
        printf("$   *\t",iCnt);
    }


}

//////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application to perform that number of $ and * on screen of int value
//
//////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                             //to store input

    printf("Enter number : ");                  //accept input from user
    scanf("%d",&iValue);

    Pattern(iValue);                            //display result

    return 0;
}

