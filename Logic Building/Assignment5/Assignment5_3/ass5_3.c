/*
Algorithm:

    START
        Accept number as iNo
        Perform to print its number line
        Display the result
    STOP
*/

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Include required header files
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////


#include<stdio.h>


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name    :   Display
// Input    :           int
// Output   :           int
// Description  :       Perform to print its number line
// Date :               30/05/2026 
// Author   :           Sharvari Gorakhnath Bhosale
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Display(
                int iNo                     //input
            )
{
    int iCnt = 0;                           //initialize counter

    for(iCnt = -4; iCnt <= iNo; iCnt++)     //perform loop
    {
        printf("%d\t",iCnt);
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application to print its number line of int value
//
//////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                         //to store input

    printf("Enter number : ");              //accept input from user
    scanf("%d",&iValue);

    Display(iValue);                        //display result

    return 0;
}

