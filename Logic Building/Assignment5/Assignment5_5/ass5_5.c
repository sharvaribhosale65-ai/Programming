/*
Algorithm:

    START
        Accept number as iNo
        Perform to print first 5 multiples of N
        Display the result
    STOP
*/

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Include required header files
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////


#include<stdio.h>


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name    :   MutiplyDisplay
// Input    :           int
// Output   :           int
// Description  :       Perform to print first 5 multiples of N
// Date :               30/05/2026 
// Author   :           Sharvari Gorakhnath Bhosale
//
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////

void MutiplyDisplay (
                        int iNo                     //input
                    )
{
    int iCnt = 0;                                   //initialize counter

    for(iCnt = 1; iCnt <= 5; iCnt++)     //perform loop
    {
        printf("%d\t",iNo * iCnt);
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application to perform to print first 5 multiples of N of int value
//
//////////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                                 //to store input

    printf("Enter number : ");                      //accept input from user
    scanf("%d",&iValue);

    MutiplyDisplay(iValue);                         //display result

    return 0;
}

