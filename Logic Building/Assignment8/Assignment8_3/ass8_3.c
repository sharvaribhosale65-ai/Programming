/////////////////////////////////////////////////////////////////////////////
//
// Include required header files
//
/////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

/////////////////////////////////////////////////////////////////////////////
//
// Function name    :   KMtoMeter
// Input    :           int
// Output   :           int
// Description  :       Convert km into meter
// Date :               30/05/2026 
// Author   :           Sharvari Gorakhnath Bhosale
//
/////////////////////////////////////////////////////////////////////////////

int KMtoMeter(int iNo)
{
    int iKm = 0;

    if(iNo < 0)
    {
        iNo = -iNo;
    }

    iKm = iNo * 1000;

    return iKm;
}

//////////////////////////////////////////////////////////////////////////////
//
// Application to convert km into meter of int value
//
//////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;
    int iRet = 0;

    printf("Enter distance : ");
    scanf("%d", &iValue);

    iRet = KMtoMeter(iValue);

    printf("KM to meter is : %d",iRet);

    return 0;
}

