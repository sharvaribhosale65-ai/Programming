#include<iostream>
using namespace std;

//////////////////////////////////////////////////////////////////////////////////////////
//
// Template for generic
//
//////////////////////////////////////////////////////////////////////////////////////////

template<class T>

//////////////////////////////////////////////////////////////////////////////////////////
//
//Function name     :   Minimum 
//Input             :   T
//Output            :   int,    float
//Description       :   Accept number from user and return smallest value using generic
//Author            :   Sharvari Gorakhnath Bhosale
//Date              :   22.07.2026
//
//////////////////////////////////////////////////////////////////////////////////////////

T Minimum(T *arr, int Size)
{
    int i = 0;

    T Min;
    Min = arr[0];

    for(i = 0; i < Size; i++)
    {
        if(arr[i] < Min)
        {
            Min = arr[i];
        }
    }
    return Min;
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//Application to accept number from user and return smallest value using generic
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 30, 40, 50};
    float brr[] = {10.1, 3.7, 9.8, 8.7};

    int iRet = 0;
    float fRet = 0.0f;

    iRet = Minimum(arr, 5);
    cout << "Minimum of int array is : " << iRet << endl;

    fRet = Minimum(brr, 4);
    cout << "Minimum of float array is : " << fRet << endl;

    return 0;
}

