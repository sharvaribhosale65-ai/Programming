template<class T>

//////////////////////////////////////////////////////////////////////////////////////////
//
//Function name     :   Multiply 
//Input             :   T
//Output            :   int,    float
//Description       :   Accept N values and reverse the contents using generic
//Author            :   Sharvari Gorakhnath Bhosale
//Date              :   22.07.2026
//
//////////////////////////////////////////////////////////////////////////////////////////

void Reverse(T *arr, int Size)
{
    int i = 0;

    for(i = Size-1; i >= 0; i--)
    {
        cout << arr[i] << "\t";
    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//Application to accept N values and reverse the contents using generic
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 30, 10, 30, 40, 10, 40, 10};

    Reverse(arr, 9);

    return 0;
}

