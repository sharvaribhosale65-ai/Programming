#include<iostream>
using namespace std;

//////////////////////////////////////////////////////////////////////////////////////////
//
// Template for generic
//
//////////////////////////////////////////////////////////////////////////////////////////

template<class T>

//////////////////////////////////////////////////////////////////////////////////////////
//
//Function name     :   Frequency 
//Input             :   T
//Output            :   int
//Description       :   Accept number from user and count frequency of any specific number using generic
//Author            :   Sharvari Gorakhnath Bhosale
//Date              :   22.07.2026
//
//////////////////////////////////////////////////////////////////////////////////////////

T Frequency(T *arr, int Size, T No)
{
    int i = 0;
    int Count = 0;

    for(i = 0; i < Size; i++)
    {
        if(arr[i] == No)
        {
            Count++;
        }
    }
    return Count;
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//Application to accept number from user and count frequency of any specific number using generic
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 30, 10, 30, 40, 10, 40, 10};
    int iRet = Frequency(arr, 9, 10);

    cout << "Frequency of 10 is : " << iRet << endl;
    return 0;
}

