#include<iostream>
using namespace std;

//////////////////////////////////////////////////////////////////////////////////////////
//
// Template for generic
//
//////////////////////////////////////////////////////////////////////////////////////////

template<class T>

//////////////////////////////////////////////////////////////////////////////////////////
//
//Function name     :   Display 
//Input             :   T
//Output            :   char,   int,    float
//Description       :   Print value of that number
//Author            :   Sharvari Gorakhnath Bhosale
//Date              :   22.07.2026
//
//////////////////////////////////////////////////////////////////////////////////////////

void Display(T Value, int Size)
{
   int i = 0;
   
   for(i = 1; i<= Size; i++)
   {
        cout << Value << "\t";
   }
   cout << endl;
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//Application to print value of that number
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    Display('M', 7);
    Display(11, 3);
    Display(3.6, 6);
    return 0;
}

