template<class T>

//////////////////////////////////////////////////////////////////////////////////////////
//
//Function name     :   AddN 
//Input             :   T
//Output            :   int,    float
//Description       :   Accept number from user and return addition of that number using generic
//Author            :   Sharvari Gorakhnath Bhosale
//Date              :   22.07.2026
//
//////////////////////////////////////////////////////////////////////////////////////////

T AddN(T *arr, int Size)
{
    if(Size <= 0)
    {
        cout << "Invalid size\n";
        cout << "Size should be greater than 0\n";
        return -1;
    }
    T Sum;

    int i = 0;
    for(i = 0; i < Size; i++)
    {
        Sum = Sum + arr[i];
    }
    return Sum;
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//Application to accept number from user and return addition of that number using generic
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 30, 40, 50};
    float brr[] = {10.0, 3.7, 9.8, 8.7};

    int iRet = 0;
    float fRet = 0.0f;

    iRet = AddN(arr, 5);
    cout << "Addition of int array is : " << iRet << endl;

    fRet = AddN(brr, 4);
    cout << "Addition of float array is : " << fRet << endl;

    return 0;
}

