#include<iostream>
using namespace std;

//////////////////////////////////////////////////////////////////////////////////////////
//
// Template for generic
//
//////////////////////////////////////////////////////////////////////////////////////////

template<class T>

//////////////////////////////////////////////////////////////////////////////////////////
//
//Function name     :   Maximum 
//Input             :   T
//Output            :   int,    float
//Description       :   Accept number from user and return largest value using generic
//Author            :   Sharvari Gorakhnath Bhosale
//Date              :   22.07.2026
//
//////////////////////////////////////////////////////////////////////////////////////////

T Maximum(T *arr, int Size)
{
    int i = 0;

    T Max;
    Max = arr[0];

    for(i = 0; i < Size; i++)
    {
        if (arr[i] > Max)
        {
            Max = arr[i];
        }
    }
    return Max;
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//Application to accept number from user and return largest value using generic
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int arr[] = {10, 20, 30, 40, 50};
    float brr[] = {10.1, 3.7, 9.8, 8.7};

    int iRet = 0;
    float fRet = 0.0f;

    iRet = Maximum(arr, 5);
    cout << "Maximum of int array is : " << iRet << endl;

    fRet = Maximum(brr, 4);
    cout << "Maximum of float array is : " << fRet << endl;

    return 0;
}

