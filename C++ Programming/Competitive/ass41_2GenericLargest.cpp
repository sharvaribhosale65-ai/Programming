#include<iostream>
using namespace std;

//////////////////////////////////////////////////////////////////////////////////////////
//
// Template for generic
//
//////////////////////////////////////////////////////////////////////////////////////////

template<class T>

//////////////////////////////////////////////////////////////////////////////////////////
//
//Function name     :   Max 
//Input             :   T
//Output            :   int,    float,  double
//Description       :   Find largest number from 3 numbers using generic
//Author            :   Sharvari Gorakhnath Bhosale
//Date              :   22.07.2026
//
//////////////////////////////////////////////////////////////////////////////////////////

T Max(T No1, T No2, T No3)
{
    if(No1 > No2 && No1 > No3)
    {
        return No1;
    }
    else if(No2 > No1 && No2 > No3)
    {
        return No2;
    }
    else
    {
        return No3;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//Application to find largest number from 3 numbers using generic
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;
    float fRet = 0.0f;
    double dRet = 0.0;

    iRet = Max(51, 21, 11);
    cout << "Maximum number is : " << iRet << endl;

    fRet = Max(21.9f, 51.4f, 11.8f);
    cout << "Maximum number is : " << fRet << endl;

    dRet = Max(11.48493, 51.489300, 21.38394);
    cout << "Maximum number is : " << dRet << endl;

    return 0;
}

