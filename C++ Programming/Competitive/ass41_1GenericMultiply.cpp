#include<iostream>
using namespace std;

//////////////////////////////////////////////////////////////////////////////////////////
//
// Template for generic
//
//////////////////////////////////////////////////////////////////////////////////////////

template<class T>

//////////////////////////////////////////////////////////////////////////////////////////
//
//Function name     :   Multiply 
//Input             :   T
//Output            :   int,    float
//Description       :   Perform multiplication operation using generic
//Author            :   Sharvari Gorakhnath Bhosale
//Date              :   22.07.2026
//
//////////////////////////////////////////////////////////////////////////////////////////

T Multiply(T No1, T No2)
{
    T Ans;

    Ans = No1 * No2;

    return Ans;
}

//////////////////////////////////////////////////////////////////////////////////////////
//
//Application to perform multiplication operation using generic
//
//////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = Multiply(10,20);
    cout << "Multiplication of int is : " << iRet << endl;

    float fRet = Multiply(10.0f, 20.0f);
    cout << "Multiplication of float is : " << fRet << endl;

    return 0;
}

