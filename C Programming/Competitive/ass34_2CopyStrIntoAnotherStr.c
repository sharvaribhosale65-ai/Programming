#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//Function name :   strCpy
//Input         :   char, char
//Output        :   void
//Description   :   Copy the contents of that string into the another string
//Date          :   24/06/2026
//Author        :   Sharvari Gorakhnath Bhosale
//
///////////////////////////////////////////////////////////////////////////////////////////

void strNCpyX(char *src, char *dest, int iCnt)
{

    while((*src != '\0') && (iCnt != 0))
    {
        *dest = *src;
        src++;
        dest++;
        iCnt--;
    }
    *dest = '\0';
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//Application to copy the contents of that string into the another string of 2 char values
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = "Marvellous Multi OS";
    char Brr[50];

    strNCpyX(Arr, Brr, 10);

    printf("%s",Brr);
    return 0;
}

