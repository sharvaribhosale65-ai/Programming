/*
Algorithm:

    START
        Accept number as iNo
        Perform to display pattern
        Display the result
    STOP
*/

////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Include required header files
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

////////////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Function name    :   Pattern
// Input    :           int
// Output   :           void
// Description  :       Perform to display pattern
// Date :               13/06/2026 
// Author   :           Sharvari Gorakhnath Bhosale
//
////////////////////////////////////////////////////////////////////////////////////////////////////////////

void Pattern(
                int iNo                                 //input
            )
{
    int iCnt = 0;

    for(iCnt = iNo; iCnt >=1; iCnt--)
    {
        printf("%d\t#\t",iCnt);
    }
}

////////////////////////////////////////////////////////////////////////////////////////////////
//
// Application to perform to display pattern of int value
//
////////////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iValue = 0;                                     //to store input

    printf("Enter number of elements : ");              //accept number from user
    scanf("%d",&iValue);

    Pattern(iValue);                                    //display result

    return 0;
}

