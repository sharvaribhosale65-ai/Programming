void displayDigit(char *str)
{
    while(*str != '\0')
    {
        if(*str >= '0' && *str <= '9')
        {
            printf("%c",*str);
        }
        str++;
    }
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//Application to display digits from string of char value
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};

    printf("Enter string : \n");
    scanf("%[^'\n']s",Arr);

    displayDigit(Arr);

    return 0;
}

