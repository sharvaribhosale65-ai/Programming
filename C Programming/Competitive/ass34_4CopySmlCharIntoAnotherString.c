#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//Function name :   strCpySmall
//Input         :   char, char
//Output        :   void
//Description   :   Copy the small characters of that string into the another string 
//Date          :   24/06/2026
//Author        :   Sharvari Gorakhnath Bhosale
//
///////////////////////////////////////////////////////////////////////////////////////////

void strCpySmall(char *src, char *dest)
{

    while(*src != '\0')
    {
        *dest = *src;
        if(*src >= 'a' && *src <= 'z')
        {
            dest++;
        }
        src++;
    }
    *dest = '\0';
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//Application to copy the small characters of that string into the another string of 2 char values
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = "Marvellous Multi OS";
    char Brr[50];

    strCpySmall(Arr, Brr);

    printf("%s",Brr);
    return 0;
}

