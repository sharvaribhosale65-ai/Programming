//
//Application to display digits from string of char value
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    int iRet = 0;

    char Arr[50] = {'\0'};

    printf("Enter string : \n");
    scanf("%[^'\n']s",Arr);

    iRet = countSpace(Arr);

    printf("Count of white space are : %d",iRet);

    return 0;
}

