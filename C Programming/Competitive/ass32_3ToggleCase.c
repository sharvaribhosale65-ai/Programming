void strToggle(char *str)
{
    char ch = '\0';

    while(*str != '\0')
    {
        if(*str >= 97 && *str <= 122)
        {
            *str = *str - 32;
        }
        else if(*str >= 65 && *str <= 90)
        {
            *str = *str + 32;
        }
        str++;
    }
}

