void displayReverse(char *str)
{
    char *start = NULL;
     
    start = str;

    while(*str != '\0')
    {
        str++;
    }

    str--;

    while(start <= str)
    {
        printf("%c",*str);
        str--;
    }
    printf("\n");
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//Application to display the reverse of string of char value
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = {'\0'};

    printf("Enter string : \n");
    scanf("%[^'\n']s",Arr);

    displayReverse(Arr);

    return 0;
}

