/*
Algorithm :
    START
        Accept a string as *src
        Accept a another string as *dest
        Copy the contents of that string into the another string 
        Display result
    STOP
*/

///////////////////////////////////////////////////////////////////////////////////////////
//
//Include required header files
//
///////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//Function name :   strCpy
//Input         :   char, char
//Output        :   void
//Description   :   Copy the contents of that string into the another string
//Date          :   24/06/2026
//Author        :   Sharvari Gorakhnath Bhosale
//
///////////////////////////////////////////////////////////////////////////////////////////

void strCpy(char *src, char *dest)
{

    while(*src != '\0')
    {
        *dest = *src;

        src++;
        dest++;
    }
    *dest = '\0';
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//Application to copy the contents of that string into the another string of 2 char values
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = "Marvellous Multi OS";
    char Brr[50];

    strCpy(Arr, Brr);

    printf("%s",Brr);
    return 0;
}

