/*
Algorithm :
    START
        Accept a string as *src
        Accept a another string as *dest
        Concat second string after first string 
        Display result
    STOP
*/

///////////////////////////////////////////////////////////////////////////////////////////
//
//Include required header files
//
///////////////////////////////////////////////////////////////////////////////////////////

#include<stdio.h>

///////////////////////////////////////////////////////////////////////////////////////////
//
//Function name :   strCatX
//Input         :   char, char
//Output        :   void
//Description   :   Concat second string after first string 
//Date          :   24/06/2026
//Author        :   Sharvari Gorakhnath Bhosale
//
///////////////////////////////////////////////////////////////////////////////////////////

void strCatX(char *src, char *dest)
{

    while(*src != '\0')
    {
        src++;
    }

    while(*dest != '\0')
    {
        *src = *dest;
        src++;
        dest++;
    }
    *src = '\0';
}

///////////////////////////////////////////////////////////////////////////////////////////
//
//Application to concat second string after first string  of 2 char values
//
///////////////////////////////////////////////////////////////////////////////////////////

int main()
{
    char Arr[50] = "Marvellous Infosystem";
    char Brr[50] = "Logic Building";

    strCatX(Arr, Brr);

    printf("%s",Arr);
    return 0;
}

